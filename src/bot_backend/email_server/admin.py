"""
Настройка административного интерфейса Django для приложения Email Server.

Этот модуль регистрирует модели в административной панели Django,
позволяя управлять пользователями и письмами через веб-интерфейс.
"""

from django.contrib import admin

# Импортируем модели из текущего приложения
from .models import Email, User

# Регистрируем модель User в админке
# Это позволяет управлять пользователями через веб-интерфейс
admin.site.register(User)

# Регистрируем модель Email в админке
# Это позволяет управлять письмами через веб-интерфейс
admin.site.register(Email)
